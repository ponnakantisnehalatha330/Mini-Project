// Keep admin-managed backend seeds as default catalog.
// Set USE_DATASET_SEEDS=true only if you explicitly want JSON dataset import.
const USE_DATASET_SEEDS = process.env.USE_DATASET_SEEDS === 'true';
const SCHEME_SEEDS = USE_DATASET_SEEDS ? DATASET_SCHEME_SEEDS : _ORIGINAL_SCHEME_SEEDS;

function initDB() {
  const db = new Database(DB_PATH);

  const addColumnIfMissing = (tableName, columnName, definition) => {
    const columns = db.prepare(`PRAGMA table_info(${tableName})`).all();
    const hasColumn = columns.some((c) => c.name === columnName);
    if (!hasColumn) {
      db.exec(`ALTER TABLE ${tableName} ADD COLUMN ${columnName} ${definition}`);
    }
  };

  db.exec(`
    CREATE TABLE IF NOT EXISTS users (
      id TEXT PRIMARY KEY,
      name TEXT NOT NULL,
      age INTEGER,
      gender TEXT,
      state TEXT,
      category TEXT,
      occupation TEXT,
      annual_income INTEGER,
      education TEXT,
      created_at TEXT DEFAULT (datetime('now'))
    );

    CREATE TABLE IF NOT EXISTS schemes (
      id TEXT PRIMARY KEY,
      name TEXT NOT NULL,
      description TEXT,
      ministry TEXT,
      benefits TEXT,
      eligibility TEXT,
      documents TEXT,
      apply_link TEXT,
      category TEXT,
      min_age INTEGER DEFAULT 0,
      max_age INTEGER DEFAULT 100,
      gender TEXT DEFAULT 'all',
      min_income INTEGER DEFAULT 0,
      max_income INTEGER DEFAULT 9999999,
      states TEXT DEFAULT 'all',
      occupation TEXT DEFAULT 'all',
      education TEXT DEFAULT 'all',
      is_upcoming INTEGER DEFAULT 0,
      launch_date TEXT,
      application_start_date TEXT,
      application_end_date TEXT,
      is_new INTEGER DEFAULT 0,
      date_added TEXT DEFAULT (datetime('now')),
      created_at TEXT DEFAULT (datetime('now'))
    );

    CREATE TABLE IF NOT EXISTS saved_schemes (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      user_id TEXT NOT NULL,
      scheme_id TEXT NOT NULL,
      saved_at TEXT DEFAULT (datetime('now')),
      UNIQUE(user_id, scheme_id)
    );

    CREATE TABLE IF NOT EXISTS user_notifications (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      user_id TEXT NOT NULL UNIQUE,
      last_seen_at TEXT,
      FOREIGN KEY(user_id) REFERENCES users(id)
    );
  `);

  addColumnIfMissing('schemes', 'states', "TEXT DEFAULT 'all'");
  addColumnIfMissing('schemes', 'occupation', "TEXT DEFAULT 'all'");
  addColumnIfMissing('schemes', 'education', "TEXT DEFAULT 'all'");
  addColumnIfMissing('schemes', 'is_upcoming', 'INTEGER DEFAULT 0');
  addColumnIfMissing('schemes', 'launch_date', 'TEXT');
  addColumnIfMissing('schemes', 'application_start_date', 'TEXT');
  addColumnIfMissing('schemes', 'application_end_date', 'TEXT');
  addColumnIfMissing('schemes', 'created_at', "TEXT DEFAULT (datetime('now'))");

  db.exec(`
    UPDATE schemes
    SET launch_date = COALESCE(launch_date, date_added),
        created_at = COALESCE(created_at, date_added),
        is_upcoming = COALESCE(is_upcoming, 0)
    WHERE launch_date IS NULL OR is_upcoming IS NULL OR created_at IS NULL
  `);

  const upsert = db.prepare(`
    INSERT INTO schemes (
      id, name, description, ministry, benefits, eligibility, documents, apply_link,
      category, min_age, max_age, gender, min_income, max_income, states, occupation,
      education, is_upcoming, launch_date, application_start_date, application_end_date,
      is_new, date_added, created_at
    ) VALUES (
      @id, @name, @description, @ministry, @benefits, @eligibility, @documents, @apply_link,
      @category, @min_age, @max_age, @gender, @min_income, @max_income, @states, @occupation,
      @education, @is_upcoming, @launch_date, @application_start_date, @application_end_date,
      @is_new, @date_added, @date_added
    )
    ON CONFLICT(id) DO UPDATE SET
      name = excluded.name,
      description = excluded.description,
      ministry = excluded.ministry,
      benefits = excluded.benefits,
      eligibility = excluded.eligibility,
      documents = excluded.documents,
      apply_link = excluded.apply_link,
      category = excluded.category,
      min_age = excluded.min_age,
      max_age = excluded.max_age,
      gender = excluded.gender,
      min_income = excluded.min_income,
      max_income = excluded.max_income,
      states = excluded.states,
      occupation = excluded.occupation,
      education = excluded.education,
      is_upcoming = excluded.is_upcoming,
      launch_date = excluded.launch_date,
      application_start_date = excluded.application_start_date,
      application_end_date = excluded.application_end_date,
      is_new = excluded.is_new,
      date_added = excluded.date_added
  `);

  const syncSeeds = db.transaction((schemes) => {
    for (const scheme of schemes) {
      upsert.run(scheme);
    }

    // Ensure catalog contains only active seed source entries.
    if (schemes.length > 0) {
      const ids = schemes.map((s) => s.id);
      const placeholders = ids.map(() => '?').join(',');
      db.prepare(`DELETE FROM schemes WHERE id NOT IN (${placeholders})`).run(...ids);
    }
  });

  syncSeeds(SCHEME_SEEDS);
  console.log('✅ Scheme catalog synced:', SCHEME_SEEDS.length, 'schemes');

  return db;
}

module.exports = { initDB, DB_PATH };
